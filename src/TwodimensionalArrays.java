
public class TwodimensionalArrays {

	public static void main(String[] args) {
// make a 2d array
		// the two brackets have the number of rows and columns 
		String[][] cars = {
				{"Lambo","toyota","bmw"},
				{"hello", "from", "the"}
		};
		System.out.println(cars.length);
		// in a 2d array car length prints out the number of rows 
	}
}
